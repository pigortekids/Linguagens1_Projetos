package exercicio_interface;

public abstract class Funcionario {
    int registro;
    String nome;
    String cpf;
    double salario;
    
    public int getRegistro(){
        return this.registro;
    }
    public int getNome(){
        return this.registro;
    }
    public int getCPF(){
        return this.registro;
    }
    public int getSalario(){
        return this.registro;
    }
}
